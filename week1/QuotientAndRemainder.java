package free90.week1;

public class QuotientAndRemainder {

	public static void main(String[] args) {
		
		int a=10, b=2;
		
		int quo,rem;
		
		quo=a/b;
		
		rem=a%b;
		
		System.out.println("Quotient of "+a+" by "+b+"="+quo);
		
		System.out.println("Remainder of "+a+" by "+b+"="+rem);

		
		
	}

}
